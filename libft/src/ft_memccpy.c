#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char	*cdest;
	unsigned char	*csrc;

	cdest = (unsigned char *)dest;
	csrc = (unsigned char *)src;
	while (n--)
	{
		*cdest++ = *csrc;
		if (*csrc++ == (unsigned char)c)
			return (cdest);
	}
	return (0);
}
